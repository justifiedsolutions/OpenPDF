package com.justifiedsolutions.openpdf.pdf.font;

/**
 * Represents the various font styles that can be applied to a font in a PDF document.
 */
public enum FontStyle {
    /**
     * No style.
     */
    NORMAL,

    /**
     * Bold style.
     */
    BOLD,

    /**
     * Italic style.
     */
    ITALIC,

    /**
     * Both bold and italic styles.
     */
    BOLD_ITALIC
}
