package com.justifiedsolutions.openpdf.pdf.font;

/**
 * Represents decorations that can be associated with text in a PDF document.
 */
public enum FontDecoration {
    /**
     * No decoration.
     */
    NONE,

    /**
     * Underline the text.
     */
    UNDERLINE,

    /**
     * Strike through the text.
     */
    STRIKE_THROUGH
}
