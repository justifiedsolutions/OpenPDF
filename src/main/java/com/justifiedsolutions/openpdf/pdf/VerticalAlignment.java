package com.justifiedsolutions.openpdf.pdf;

/**
 * Represents the different horizontal alignments that can be utilized on text.
 */
public enum VerticalAlignment {
    /**
     * Top aligned.
     */
    TOP,
    /**
     * Middle aligned.
     */
    MIDDLE,
    /**
     * Bottom aligned.
     */
    BOTTOM,
    /**
     * Aligned with the baseline.
     */
    BASELINE
}
